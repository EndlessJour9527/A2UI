"""Interface definitions and stubs for the future auto-annotation pipeline."""

from __future__ import annotations

from abc import ABC, abstractmethod
from typing import Any, Protocol

from .studio_types import StudioAnnotation, StudioCaseResult


class AnnotationProvider(Protocol):
    """Protocol defining the interface for all automated annotators."""

    def annotate(self, result: StudioCaseResult) -> list[StudioAnnotation]:
        """Generate annotations for a given case execution result.

        Args:
            result: The materialized case result to evaluate.

        Returns:
            A list of StudioAnnotation objects representing findings, scores,
            or dispositions generated by the annotator.
        """
        ...


class BaseAnnotator(ABC):
    """Abstract base class providing shared utilities for concrete annotators."""

    def __init__(self, name: str, config: dict[str, Any] | None = None):
        self.name = name
        self.config = config or {}

    @abstractmethod
    def annotate(self, result: StudioCaseResult) -> list[StudioAnnotation]:
        """Subclasses must implement this method to execute annotation logic."""
        pass


